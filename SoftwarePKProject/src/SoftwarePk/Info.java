package SoftwarePk;

public class Info {
	private String Name;
	private int Number;
	private String EMail;
	
	public void setName(String Name){
		this.Name = Name;
	}
	public String getName(){
		return this.Name;
	}
	
	public void setNumber(int Number){
		this.Number = Number;
	}
	public int getNumber(){
		return this.Number;
	}
	
	public void setEMail(String EMail){
		this.EMail = EMail;
	}
	public String getEMail(){
		return this.EMail;
	}

}
